public class Java_08 {
    public static void main(String args[]){
       //operators
       int a=4;
       int b=45;
       System.out.println(a+b); 
       System.out.println(a-b); 
       System.out.println(a*b); 
       System.out.println(a/b); 
       System.out.println(a%b); 
       System.out.println(a==b); 
       System.out.println(a>b); 
       System.out.println(a>=b); 
       System.out.println(a<=b); 
       System.out.println(a<b); 
       System.out.println(a!=b); 
       System.out.println(a!=b && a>=b); 
       System.out.println(a!=b || a>=b); 

    }
}
